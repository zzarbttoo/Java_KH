DROP TABLE SCORE;

CREATE TABLE MYSCORE(

MYNAME VARCHAR2(500) PRIMARY KEY,
MYKOR NUMBER NOT NULL,
MYENG NUMBER NOT NULL,
MYMATH NUMBER NOT NULL,
MYSUM NUMBER NOT NULL,
MYAVG NUMBER NOT NULL,
MYGRADE VARCHAR(2) CHECK (MYGRADE IN ('A', 'B' , 'C', 'D', 'F'))
);


SELECT *
FROM 
(SELECT ROWNUM AS R, MYNAME, MYKOR, MYENG, MYAVG, MYGRADE FROM

	(SELECT MYNAME, MYKOR, MYENG, MYMATH, MYSUM, MYSUM, MYGRADE FROM MYSCORE
	ORDER BY MYAVG DESC)
)WHERE R = 2;