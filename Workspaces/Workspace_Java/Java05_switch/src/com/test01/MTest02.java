package com.test01;

public class MTest02 {

	
	public static void main(String[] args) {
		//switch에는 정수형, 문자형, 문자열(!) 이 들어갈 수 있다
		String s= "한국";
		
		switch(s) {
		case "한국" :
			System.out.println(); //siso + ctrl + spacebar  //sout + ctrl + spacebar //ctrl space bar -> 자동완성
			break;
		case "중국" : 
			System.out.println("china");
			break;
		case "일본":
			System.out.println("japan");
			break;
			
			
		}
		
	}
	
		
}
